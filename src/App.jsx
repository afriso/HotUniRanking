function App() {
  // 用于记录当前展开的热点，格式：{"大学名-索引": true}
  const [openHot, setOpenHot] = useState({});
  // 搜索关键词
  const [search, setSearch] = useState("");
  // 搜索结果
  const [searchResults, setSearchResults] = useState([]);
  // 存储大学卡片ref
  const univRefs = useRef({});
  // 搜索逻辑
  const handleSearch = (e) => {
    const value = e.target.value;
    setSearch(value);
    if (!value.trim()) {
      setSearchResults([]);
      return;
    }
    // 搜索大学名
    const results = sortedUniversities
      .map((u, uniIdx) => ({ name: u.name, uniIdx }))
      .filter((u) => u.name.includes(value));
    setSearchResults(results);
  };

  // 滚动到大学卡片
  const scrollToUniv = (uniIdx) => {
    const ref = univRefs.current[uniIdx];
    if (ref) {
      ref.scrollIntoView({ behavior: "smooth", block: "center" });
    }
  };

  // 切换热点展开/收起
  const toggleHot = (uniName, idx) => {
    const key = `${uniName}-${idx}`;
    setOpenHot((prev) => ({ ...prev, [key]: !prev[key] }));
  };

  // 时间字符串转时间戳
  const getTime = (t) => new Date(t).getTime();

  // 先对每个大学的热点事件按时间降序排序，再对大学排序
  const sortedUniversities = [...universities]
    .map((u) => ({
      ...u,
      hots: [...u.hots].sort((a, b) => getTime(b.time) - getTime(a.time)),
    }))
    .sort((a, b) => {
      const aLatest = a.hots.length > 0 ? getTime(a.hots[0].time) : 0;
      const bLatest = b.hots.length > 0 ? getTime(b.hots[0].time) : 0;
      return bLatest - aLatest;
    });

  return (
    <>
      {/* 悬浮顶部搜索栏 */}
      <div
        style={{
          position: "fixed",
          top: 0,
          left: 0,
          width: "100%",
          background: "rgba(255,255,255,0.97)",
          zIndex: 100,
          boxShadow: "0 2px 8px rgba(0,0,0,0.06)",
          padding: "12px 0 8px 0",
          display: "flex",
          flexDirection: "column",
          alignItems: "center",
        }}
      >
        <div style={{ maxWidth: 400, width: "100%", position: "relative" }}>
          <input
            type="text"
            className="search-input"
            placeholder="搜索大学..."
            value={search}
            onChange={handleSearch}
            style={{
              borderRadius: 16,
              padding: "8px 16px",
              width: "100%",
              fontSize: 16,
              border: "1px solid #ddd",
              outline: "none",
            }}
          />
          {/* 搜索结果列表 */}
          {search && searchResults.length > 0 && (
            <div
              className="search-result-list"
              style={{
                background: "#fff",
                borderRadius: 12,
                boxShadow: "0 2px 8px rgba(0,0,0,0.08)",
                marginTop: 8,
                padding: 8,
                maxHeight: 220,
                overflowY: "auto",
                position: "absolute",
                zIndex: 10,
                width: "100%",
              }}
            >
              {searchResults.map((item, i) => (
                <div
                  key={i}
                  className="search-result-item"
                  style={{
                    padding: "6px 10px",
                    borderRadius: 8,
                    cursor: "pointer",
                    color: "#2d3a4b",
                  }}
                  onClick={() => scrollToUniv(item.uniIdx)}
                >
                  <b>{item.name}</b>
                </div>
              ))}
            </div>
          )}
        </div>
      </div>
      {/* 页面内容加上顶部间距 */}
      <div className="container" style={{ marginTop: 80 }}>
        <h1>热点大学排名</h1>
        <div style={{ fontSize: "1.15em", color: "#666", marginBottom: 18 }}>
          实时展示与排序全国高校热点事件，点击可查看详情
        </div>
        {sortedUniversities.map((u, uniIdx) => (
          <div
            className="univ-card"
            key={u.name}
            ref={(el) => (univRefs.current[uniIdx] = el)}
          >
            <h2>{u.name}</h2>
            <div className="hot-list">
              {u.hots.map((hot, idx) => {
                const key = `${u.name}-${idx}`;
                const isOpen = openHot[key];
                return (
                  <div
                    className={`hot-card${isOpen ? " open" : ""}`}
                    key={key}
                    onClick={() => toggleHot(u.name, idx)}
                    tabIndex={0}
                    style={{ cursor: "pointer" }}
                  >
                    <div className="hot-header">
                      <span
                        className={`impact-badge impact-${hot.impact}`}
                        title={hot.impact}
                      >
                        {hot.impact}
                      </span>
                      <span className="hot-title">{hot.title}</span>
                      <span className="hot-time">{hot.time}</span>
                    </div>
                    {isOpen && <div className="hot-detail">{hot.detail}</div>}
                  </div>
                );
              })}
            </div>
          </div>
        ))}
      </div>
    </>
  );
}
export default App;
